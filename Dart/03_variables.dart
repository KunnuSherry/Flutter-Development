void main() {
  //variables
  
  //For Integers
  int a = 10;
  int b = 20;
  print(a+b);
  print(a);
  
  //For Decimal Numbers
  double c = 10.10;
  double d = 11.22;
  print(c*d);
  print(c);
  
  //For words, chars
  String firstValue = "H";
  print(firstValue);
  
  //Booleans
  bool f = false;
  print(f);
  
  //Dynamic
  dynamic cd = 10;
  print(cd);
  
}
